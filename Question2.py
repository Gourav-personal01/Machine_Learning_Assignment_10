# Q2. Discuss the assumptions of linear regression. How can you check whether these assumptions hold in
# a given dataset?

# These assumptions include:

# Linearity: The relationship between the independent variables and the dependent variable should be 
# approximately linear. This means that changes in the independent variables should lead to proportional changes in the dependent variable.

# Independence of Errors: The errors (residuals) should be independent of each other, meaning that the error associated with one observation 
# should not be influenced by the errors of other observations

# Homoscedasticity: The variance of the errors should be constant across all levels of the independent variables. In other words, 
# the spread of the residuals should be roughly consistent as you move along the regression line
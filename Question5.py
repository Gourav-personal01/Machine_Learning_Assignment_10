# Q5. Describe the multiple linear regression model. How does it differ from simple linear regression?

# Answer - 
# multiple linear regression contains the multiple independent features and based on that features it will predict the target variable.

# In SLR , The relationship is modeled as a straight line, represented by the equation: Y = aX + b, where Y is the dependent variable, 
#    X is the independent variable, a is the slope, and b is the intercept. Whearas in MLR, The relationship is modeled as a hyperplane in a multidimensional space, represented by the equation: Y = aX1 + bX2 + cX3 + ... + d, 
#   where Y is the dependent variable, X1, X2, X3, etc., are the independent variables, and a, b, c, etc., are coefficients.

# 3. SLR is suitable when there is a single predictor variable that is believed to have a linear relationship with the target variable. whereas the
#   MLR is  suitable when there are multiple predictor variables that may collectively influence the target variable.
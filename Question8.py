# Q8. What are the advantages and disadvantages of polynomial regression compared to linear
# regression? In what situations would you prefer to use polynomial regression?

# Advantages - 
# In linear regression, predictors are used as-is, without transformation. In polynomial regression, predictors are transformed by raising them to higher powers.
# Linear regression is a simpler model that fits a straight line to the data. Polynomial regression is more flexible and can fit curves of varying complexity, depending on the polynomial degree.
# Linear regression assumes a linear relationship, while polynomial regression allows for nonlinear relationships

